// it will add two numbers---->
function add(a, b) {
    console.log(a + b);
}

// it will multiply two numbers---->
function multiply(a, b) {
    console.log(a * b);
}

// it will subtract two numbers---->
function sub(a, b) {
    console.log(a - b);
}

// it will divide two numbers---->
function divide(a, b) {
    console.log(a / b);
}

//functions calling---->
add(3, 4); //output should be 7
multiply(5, 8); //output should be 40
sub(8, 2); //output should be 6
divide(23, 6); //output should be 3